package Pessoas;

/**
 *
 * @author Aluno
 */
public class Pessoa {
    protected String nome;
    protected int idade;
    protected String adress;

    public Pessoa(String nome, int idade, String adress) {
        this.nome = nome;
        this.idade = idade;
        this.adress = adress;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getAdress() {
        return adress;
    }

    public void setAdress(String adress) {
        this.adress = adress;
    }
}